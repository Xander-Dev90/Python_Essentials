# de cada elemento de la lista

# Creación de una lista con múltiples tipos de datos
frutas = ["manzana", "pera", "uva", "fresa", "kiwi"]
for index, fruta in enumerate(frutas):
    print(f"Fruta {index}: {fruta}")

